					  <p class="event-detail-text">
						Sebagai salah satu solusi untuk mengembangkan teknologi data pada pendidikan Indonesia agar bisa menciptakan sebuah era generasi pendidikan cerdas dan mengerti dalam memahami hadirnya teknologi data sehingga dapat mencetak generasi yang penuh inovasi dan produktivitas. Adapun spesifikasi acara dalam DINAMIK 12 yaitu Syukuran Dinamik, Maint Event, Talkshow Inspiratif, Workshop Digitalisasi Guru, Seminar Nasional dan berbagai lomba seperti Lomba Desain Grafis (LDG), Lomba Cipta Web (LCW), Olimpiade Computer Science (OCS), Personal Computer Assembling (PCA), Computer Science Programming Competition (CSPC), Lomba Cipta Animasi (LCA), Line Follower, DINAMIK Star dan Kompetisi Jaringan. 
					  </p>
					  <p class="event-detail-text">
